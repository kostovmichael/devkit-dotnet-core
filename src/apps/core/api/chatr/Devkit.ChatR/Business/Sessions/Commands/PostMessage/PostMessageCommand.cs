namespace Devkit.ChatR.Business.Sessions.Commands.PostMessage
{
    using Devkit.ChatR.Business.ViewModels;
    using Devkit.Patterns.CQRS.Command;

    /// <summary>
    /// Post message command.
    /// </summary>
    public class PostMessageCommand : CommandRequestBase<MessageVM>
    {
        /// <summary>
        /// Gets or sets the message.
        /// </summary>
        /// <value>
        /// The message.
        /// </value>
        public string Message { get; set; }

        /// <summary>
        /// Gets or sets the reply to.
        /// </summary>
        /// <value>
        /// The reply to.
        /// </value>
        public string ReplyTo { get; set; }

        /// <summary>
        /// Gets or sets the session identifier.
        /// </summary>
        /// <value>
        /// The session identifier.
        /// </value>
        public string SessionId { get; set; }

        /// <summary>
        /// Gets or sets the name of the user.
        /// </summary>
        /// <value>
        /// The name of the user.
        /// </value>
        public string UserName { get; set; }
    }
}